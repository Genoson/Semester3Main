// a practice and note repository for the os module of javascript

const os = require('os');

// os is an object.
// it contains many properties and methods.
// methods include:
// .arch() - returns the architecture of the processor.
// .cpus() - returns an array of objects containing information about each CPU.
// .endianness() - returns the endianness of the processor.
// .freemem() - returns the amount of free memory available.
// .homedir() - returns the home directory of the current user.
// .hostname() - returns the hostname of the machine.
// .loadavg() - returns an array of numbers representing the 1, 5, and 15 minute load averages.
// .networkInterfaces() - returns an object containing the network interfaces of the machine.
// .platform() - returns the operating system platform.
// .release() - returns the operating system release.
// .tmpdir() - returns the path of the temporary directory.
// .totalmem() - returns the total amount of memory.
// .type() - returns the operating system type.

